
/**
 * TileShape contains all of the possible shapes for the tiles in the Tile class
 * LU is Left Up curve, RU is right up curve, LD is Left down curve, RD is right 
 * down curve, H is horizontal, V is vertical, S is start, E is end
 *  a 's' indicates a star on the grid. a 'S' indicates start, and 'E' indicates
 *  end. B is blank. N is nothing, M indicates cannot move
 * @author Chris, Ronald, Matt, Ryan, Luis
 * @version 1.0
 */
public enum TileShape
{
    LU, RU, LD, RD, H, V, B, N, RS, LS, DS, US, RE, LE, DE, UE;
}